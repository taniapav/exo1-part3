//Je declare ma fonction.Elle permet d'afficher la bordure rouge autour de l'image.
function changeBorder(){
  document.getElementById('image1').style.border = '3px solid red';
}
//Je declare ma fonction.Elle permet de retirer la bordure rouge autour de l'image.
function returnBorder(){
  document.getElementById('image1').style.border = 'initial';
}
